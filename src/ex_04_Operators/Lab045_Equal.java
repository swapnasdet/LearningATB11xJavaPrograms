package ex_04_Operators;

public class Lab045_Equal {
    public static void main(String[] args) {
        // ==  compare values
        System.out.println(10 == 10); // true
        System.out.println(10 < 10); // false
        System.out.println(10 == 20); // false
        System.out.println(10 >= 10); // true


    }
}
